// Compute totals for dashboard use
export function computeInventoryKPIs(items: InventoryItem[]) {
  const active = items.filter(i => i.status !== 'sold');
  const totalValue = active.reduce((sum, i) => sum + i.current_market_price * i.quantity, 0);
  const totalCost  = active.reduce((sum, i) => sum + i.cost_basis * i.quantity, 0);
  return {
    totalValue,
    totalCost,
    unrealizedProfit: totalValue - totalCost,
    itemCount: active.length,
    avgROI: totalCost > 0 ? ((totalValue - totalCost) / totalCost) * 100 : 0,
  };
}
