// ─── Monthly aggregation helper ──────────────────────────────

export function groupByMonth(transactions: SoldTransaction[]) {
  const map = new Map<string, { revenue: number; profit: number; cost: number; sales_count: number }>();

  transactions.forEach(t => {
    const month = t.date_sold.slice(0, 7); // "2025-01"
    const existing = map.get(month) ?? { revenue: 0, profit: 0, cost: 0, sales_count: 0 };
    map.set(month, {
      revenue:     existing.revenue + t.sold_price,
      profit:      existing.profit + t.net_profit,
      cost:        existing.cost + t.cost_basis,
      sales_count: existing.sales_count + 1,
    });
  });

  return Array.from(map.entries())
    .map(([month, data]) => ({ month, ...data }))
    .sort((a, b) => a.month.localeCompare(b.month));
}

export function computeSalesKPIs(transactions: SoldTransaction[]) {
  const totalRevenue      = transactions.reduce((s, t) => s + t.sold_price, 0);
  const totalProfit       = transactions.reduce((s, t) => s + t.net_profit, 0);
  const totalFees         = transactions.reduce((s, t) => s + t.fees, 0);
  const totalShipping     = transactions.reduce((s, t) => s + t.shipping_cost, 0);
  const totalCost         = transactions.reduce((s, t) => s + t.cost_basis, 0);

  return {
    totalRevenue,
    totalProfit,
    totalFees,
    totalShipping,
    totalCost,
    profitMargin: totalRevenue > 0 ? (totalProfit / totalRevenue) * 100 : 0,
    avgProfit:    transactions.length > 0 ? totalProfit / transactions.length : 0,
    salesCount:   transactions.length,
  };
}
